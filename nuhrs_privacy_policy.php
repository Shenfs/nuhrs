            <h3>Applicability  of This Privacy Policy</h3>
            <p class="text-justify">Our Privacy Policy attests to our commitment to privacy and demonstrates the ways we ensure that patient privacy is protected.  Our Privacy Policy applies to the personal health information of all our patients that is in our possession and control.</p>
           <h3>What is Personal Health Information?</h3>
           <p class="text-justify">Personal health information is a sensitive personal informationabout an individual’s physical or mental health (including but is not limited tomedical history), and the form of health care provided to the individual.</p>
           <h3>The 10 principle of Privacy Policy</h3>
           <ul>
            <p>1. Accountability - Every doctor and staff member who is part of the Practice is accountable for safeguarding the personal information within their scope of responsibility.  The employees are educated about the significance of privacy and receive periodic updates on our Privacy Policy and related topics.</p>
            <p>2. Identifying Purposes: Why We Collect Information - We request information from you to establish a connection and fulfill your medical requirements. We primarily  obtain your information either directly from you or from other healthcare providers authorized by you to disclose it to us. You have the right to know how we use yourinformation, which is outlined in the Privacy Statement available ontheNUFairview Privacy Statement page.</p>
            <p>3. Consent - You have the right to control the use and disclosure of your personal health information. While consent for treatment implies  consent for most healthcare purposes, express consent must always be in writing. Your written consent will be documented by the Privacy Officer in your medical records and shared with relevant healthcare providers and their staff.Patients who wish to revoke consent for the disclosure of their PHI must sign the Consent to Withdrawal Form. It is important to note that the revocation only applies to information that has already been provided and not to information that may be provided in the future. Certain PHI collections, uses, and disclosures are permitted by law and may override the consent directive. Additionally, healthcare providers may override the consent directive in emergencies.  However, revoking consent may result in delays or reduced quality of non-emergency  healthcare due to incomplete patient information. Your completed Consent to Withdrawal Form will be recorded by the Privacy Officer in your medical records and shared with relevant healthcare providers and their staff.</p>
            <p> 4. Limiting  Collection - We obtain information through fair and legal methods and collect only the information required for providing your medical care.</p>
            <p> 5. Limiting  Use, Disclosure and Retention - The information we collect from you is used solely for the purposes described. If we intend to use your information for purposes beyond the scope of the Privacy Statement, we will obtain your consent.We do not sell patient lists or personal information to any third parties. However, we may disclose your personal health information to consultants and suppliers  of the Practice who agree to abide by our Privacy Policy and use the information only as necessary for their business services  or support to this Practice.We will retain your information only for as long as required for the purposes we describe, and any information that is no longer needed will be destroyed. However, we may need to retain certain information for a longer period due to the possibility  of future claims.In order to release information, patients may be required to sign and date a Consent to Disclose PHI Form and pay a fee based on the current OMA rates.</p>
           <p>6. Accuracy - Our aim is to make sure that every decision related to your personal information is taken based on accurate and up-to-date information. Although we will make every effort to use correct information, it's also important for you to provide us with complete and relevant information and inform us of any updates.</p>
           <p>7. Safeguards: Protecting Your Information - We take appropriate measures to safeguard and secure your information. Personal information can only be accessed by physicians, employees,  and other authorized agents who require access to perform their duties or are authorized by law. We share your information with healthcare providers on your behalf who arealso bound by law and ethics to protect your privacy. Any other organizations or agents who need access to your information must agree to follow our Privacy Policy and may be required to sign a contract to that effect. We only share the necessary information to perform the services,  and we require  them not to use or disclose the information for any other purpose. Our computer systems are protected by passwords, and only authorized individuals can access the secure systems and databases.</p>
           <p>8. Openness: Keeping You Informed - The NUHRS has created an easy-to-understand Privacy Policy to keep you informed about our data protection practices. You can access a copy of the policy on our website by clicking on the link <a href="nuhrs-privacy-policy_v3.pdf" target="_blank">clinic.nuhrs.online</a></p>
           <p>9. Access and Correction - We will usually allow you to access the information that we have on record about you, in compliance  with the law and medical professionals' guidelines, except for certain circumstances. We may charge you a fee for this service and will notify you before processing  your request if sucha fee applies. If you discover factual inaccuracies  in your personal  health information, please inform us immediately, and we will make the necessary adjustments. Please note that we are not obligated to rectify clinical  observations or opinions expressed in good faith.</p>
           <p>10. Challenging Compliance - We encourage you to contact us with any questions or concerns you might have about your privacy or our Privacy Policy.  We will investigate and respond to your concerns about any aspect of our handling of your information. In most cases, an issue is resolved simply  by telling us about it and discussing it.  You can reach us at:</p>
